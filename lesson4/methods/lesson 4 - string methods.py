print(string.capitalize())  # –ü–µ—Ä–≤–∞—è –±—É–∫–≤–∞ –∑–∞–≥–ª–∞–≤–Ω–∞—è


# –£–¥–∞–ª—è–µ—Ç –ª–∏—à–Ω–∏–µ –ø—Ä–æ–±–µ–ª—ã (—Å–ª–µ–≤–∞ –∏ —Å–ø—Ä–∞–≤–∞)
print("# –£–¥–∞–ª—è–µ—Ç –ª–∏—à–Ω–∏–µ –ø—Ä–æ–±–µ–ª—ã")
s = "   –∏–º—è —Ñ–∞–º–∏–ª–∏—è "
print("–ë–µ–∑ —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è:", s)
formatted_s = s.strip()
print("–ü–æ—Å–ª–µ —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è:", formatted_s)


# ---------- –†–∞–∑–¥–µ–ª—è–µ—Ç —Å—Ç—Ä–æ–∫—É –Ω–∞ —Å–ø–∏—Å–æ–∫ –ø–æ–¥—Å—Ç—Ä–æ–∫ -------------
#         ‚ùóÔ∏è‚ùóÔ∏è–ü–æ —É–º–æ–ª—á–∞–Ω–∏—é —ç—Ç–æ —Å–∏–º–≤–æ–ª –ø—Ä–æ–±–µ–ª–∞‚ùóÔ∏è‚ùóÔ∏è

print("# –†–∞–∑–¥–µ–ª—è–µ—Ç —Å—Ç—Ä–æ–∫—É –Ω–∞ —Å–ø–∏—Å–æ–∫ –ø–æ–¥—Å—Ç—Ä–æ–∫")
print("–†–∞–∑–¥–µ–ª—è–µ—Ç —Å—Ç—Ä–æ–∫—É –Ω–∞ —Å–ø–∏—Å–æ–∫ –ø–æ–¥—Å—Ç—Ä–æ–∫".split())
# ['–†–∞–∑–¥–µ–ª—è–µ—Ç', '—Å—Ç—Ä–æ–∫—É', '–Ω–∞', '—Å–ø–∏—Å–æ–∫', '–ø–æ–¥—Å—Ç—Ä–æ–∫']

domains = "google.com,ya.ru,example.com"
print(domains.split(","))                # –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å–ø–∏—Å–æ–∫
