# ['google.com;ya.ru;example.com']


# ========== –°–æ–±–∏—Ä–∞–µ–º —Å—Ç—Ä–æ–∫—É –∏–∑ —Å–ø–∏—Å–∫–∞ ‚ùóÔ∏è–°–¢–†–û–ö‚ùóÔ∏è=============

print("# –°–æ–±–∏—Ä–∞–µ–º —Å—Ç—Ä–æ–∫—É –∏–∑ —Å–ø–∏—Å–∫–∞")

