print(string.count("–≠–¢–û"))  # 1

# –í–°–ï –ú–ï–¢–û–î–´ –ù–ò–ñ–ï –ù–ï –í–õ–ò–Ø–Æ–¢ –ù–ê –ó–ù–ê–ß–ï–ù–ò–ï `string`

print(string.lower())  # –í –Ω–∏–∂–Ω–∏–π —Ä–µ–≥–∏—Å—Ç—Ä
print(string.upper())  # –í –≤–µ—Ä—Ö–Ω–∏–π —Ä–µ–≥–∏—Å—Ç—Ä
