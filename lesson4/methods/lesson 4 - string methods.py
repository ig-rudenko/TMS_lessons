# ‚ùóÔ∏è‚ùóÔ∏è–°–¢–†–û–ö–ê –≠–¢–û –ù–ï–ò–ó–ú–ï–ù–Ø–ï–ú–´–ô –¢–ò–ü‚ùóÔ∏è‚ùóÔ∏è

string = "‚ùóÔ∏è‚ùóÔ∏è–°–¢–†–û–ö–ê —ç—Ç–æ –ù–ï–ò–ó–ú–ï–ù–Ø–ï–ú–´–ô –¢–ò–ü‚ùóÔ∏è‚ùóÔ∏èü§ó"

# –ö–æ–ª-–≤–æ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–π –ü–û–î–°–¢–†–û–ö–ò
print(string.count("–´"))    # 1
